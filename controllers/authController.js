export const updateProfileController = async (req, res) => {
  try {
    const { name, email, password, address, phone } = req.body; //aa badhu  tp req ma mali  jashe
    const user = await userModel.findById(req.user._id); //req ma user che and user ma teni id tena base par find karihu
    //password
    if (password && password.length < 6) {
      return res.json({ error: "Passsword is required and 6 character long" });
    }
    const hashedPassword = password ? await hashPassword(password) : undefined; //password male to hash kari dyo or undefined kri didhu
    const updatedUser = await userModel.findByIdAndUpdate(
      req.user._id,
      {
        name: name || user.name, //name male to name thi name ne update kro or old user.name em nam rakho
        password: hashedPassword || user.password,
        phone: phone || user.phone,
        address: address || user.address,
      },
      { new: true }
    );
    res.status(200).send({
      success: true,
      message: "Profile Updated SUccessfully",
      updatedUser,
    });
  } catch (error) {
    console.log(error);
    res.status(400).send({
      success: false,
      message: "Error WHile Update profile",
      error,
    });
  }
};

